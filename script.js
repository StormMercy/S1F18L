// Функция для создания полей ввода букв
function createLetterInputs(count) {
  letterInputs.innerHTML = ''; // Очищаем предыдущие поля ввода
  
  for (let i = 0; i < count; i++) {
    const div = document.createElement('div');
    div.classList.add('letterInput');
    div.innerHTML = `
      <label for="letter${i + 1}">${i + 1}:</label>
      <input type="text" class="letter squareInput" id="letter${i + 1}" maxlength="1" tabindex="${i + 1}">
    `;
    letterInputs.appendChild(div);
  }

  // Добавляем обработчики событий для перемещения между полями ввода при помощи клавиш "вверх" и "вниз"
  const letterInputsList = document.querySelectorAll('.letterInput input');
  letterInputsList.forEach((input, index) => {
    input.addEventListener('keydown', function(event) {
      const currentTabIndex = parseInt(input.getAttribute('tabindex'));
      let nextIndex;
      if (event.key === 'ArrowLeft') {
        nextIndex = currentTabIndex - 1;
      } else if (event.key === 'ArrowRight') {
        nextIndex = currentTabIndex + 1;
      }
      if (nextIndex >= 1 && nextIndex <= count) {
        const nextInput = document.querySelector(`.letterInput input[tabindex="${nextIndex}"]`);
        nextInput.focus();
      }
    });
  });
}

