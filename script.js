function checkWord() {{
  const letters = document.querySelectorAll('.letter');
  const inputWord = Array.from(letters).map(input => input.value.toLowerCase() || '*').join('');

  const excludeLettersInput = document.getElementById('excludeLetters');
  const excludeLetters = excludeLettersInput.value.toLowerCase().replace(/\s/g, '');

  const wordPerRowInput = document.getElementById('wordPerRow');
  const wordsPerRow = parseInt(wordPerRowInput.value);

  const usedLetters = {};
  letters.forEach(letter => {
    if (letter.value.trim() !== '') {
      const letterValue = letter.value.toLowerCase();
      if (usedLetters[letterValue]) {
        usedLetters[letterValue]++; // увеличиваем счетчик повторений буквы
      } else {
        usedLetters[letterValue] = 1; // иначе инициализируем счетчик повторений буквы
      }
    }
  });

  const matchedWords = words[inputWord.length].filter(word => {
    if (inputWord.length <= word.length) {
      const lowerCaseWord = word.toLowerCase();
      return inputWord.split('').every((char, index) => char === '*' || char === lowerCaseWord[index]) &&
             !Array.from(excludeLetters).some(excludeLetter => lowerCaseWord.includes(excludeLetter)) &&
             !Array.from(Object.keys(usedLetters)).some(usedLetter => {
               const count = lowerCaseWord.split(usedLetter).length - 1; // подсчитываем количество вхождений буквы в слове
               return count > usedLetters[usedLetter]; // проверяем, что буква не встречается более раза
             });
    }
    return false;
  });

  // Приводим все слова к нижнему регистру
  const lowerCaseMatchedWords = matchedWords.map(word => word.toLowerCase());

  // Удаляем повторяющиеся слова с учетом регистра первой буквы
  const uniqueMatchedWords = [];
  const uniqueWordsSet = new Set();

  lowerCaseMatchedWords.forEach((word, index) => {
    if (!uniqueWordsSet.has(word)) {
      uniqueMatchedWords.push(matchedWords[index].toLowerCase());
      uniqueWordsSet.add(word);
    }
  });

  const resultContainer = document.getElementById('result');
  if (uniqueMatchedWords.length > 0) {
    let tableHTML = '<div class="table">';
    for (let i = 0; i < uniqueMatchedWords.length; i += wordsPerRow) {
      tableHTML += '<div class="row">';
      for (let j = i; j < i + wordsPerRow && j < uniqueMatchedWords.length; j++) {
        tableHTML += `<div class="cell">${uniqueMatchedWords[j]}</div>`;
      }
      tableHTML += '</div>';
    }
    tableHTML += '</div>';
    resultContainer.innerHTML = tableHTML;
  } else {
    resultContainer.innerHTML = 'Нет совпадений со словами из списка.';
  }
}



  
  // Очищаем уведомление
  const notificationContainer = document.getElementById('notificationContainer');
  notificationContainer.textContent = '';
}


